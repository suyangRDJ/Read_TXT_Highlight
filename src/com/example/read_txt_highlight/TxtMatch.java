package com.example.read_txt_highlight;

public class TxtMatch {

	private int match;

	private String content;

	public int getMatch() {
		return match;
	}

	public void setMatch(int match) {
		this.match = match;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
